<p>Gordon Floyd became CEO of Children’s Mental Health Ontario in January 2004, after 10 years as Vice-president, Public Affairs and Interim CEO at the Canadian Centre for Philanthropy.</p>
<p>He has a background in politics and business, having served as Director of Policy for the Liberal Party of Canada, Chief of Staff to three Leaders of the Opposition in Ontario, president of a mid-size government relations consulting firm, co-founder of Canada's largest fundraising consulting business, a partner in a Toronto harbour tour boat business, and a producer of both special events and professional theatre.</p>
<p>Gordon has served in numerous volunteer capacities, including as Board Chair of Family Service Toronto, the AIDS Committee of Toronto, and the Vancouver based Institute on Media, Policy and Civil Society. He currently serves as a director of the Agora and Pemsel Case Foundations, on the editorial board of The Philanthropist, on the Strategy &amp; Vision Committee for Big Brothers Big Sisters of Canada, on the advisory council for the Nonprofit Management and Leadership Program at York University’s School of Business, on the Advisory Committee for Imagine Canada’s National Engagement Strategy and on the Steering Committee for the Muttart Consultations.</p>
<p>Gordon is a recipient of the Award of Excellence in Policy Leadership from Carleton University.</p>
