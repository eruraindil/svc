<h2>Student Volunteer Connections Website www.dosomuch.ca:</h2>
<ul>
	<li>Coordinate and maintain all operations of SVC’s home website</li>
	<li>Develop, upgrade and maintain www.dosomuch.ca to maximize user friendliness and ability to effectively promote volunteerism</li>
	<li>Gather and enter useful links onto website that encourage student volunteerism</li>
	<li>Develop metrics by which to assess website traffic including page uploads and usage rates</li>
	<li>In collaboration with the Community Relations Coordinator, research, develop, and organize the volunteer resources section (volunteer management, tips, etc)</li>
</ul>

<h2>Social Media:</h2>
<ul>
	<li>Collaborate with Director, Projects Coordinator and Community Relations Coordinator to ensure regular online presence on SVC’s social media accounts including Facebook and Twitter</li>
</ul>

<h2>Online Organization and Opportunity Database:</h2>
<ul>
	<li>Network with University and Community organizations with intention of highlighting online database and generating traffic</li>
	<li>Manage and expand the capacity of online database to ensure usability</li>
</ul>

<h2>Promotions Material Creation:</h2>
<ul>
	<li>Assist staff team in creation of promotions material for events and other initiatives</li>
</ul>

<h2>Further Duties:</h2>
<ul>
	<li>Attend staff and board training sessions as required</li>
	<li>Maintain day-to-day operations of SVC office, including answering phone, maintaining email presence, etc.</li>
	<li>Work in close collaboration with all other members of SVC’s staff and volunteer teams</li>
	<li>Required to complete thorough transition report and document library to be provided to incoming Webmaster</li>
</ul>
