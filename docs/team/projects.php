<h1>Ian Shaule - Projects Coordinator SVC, 2012 - 2013</h1>

<h2>HELP Series:</h2>
<ul>
	<li>Coordinate and develop various one-time group volunteering activities for HELP: Social Action Now programming</li>
	<li>Record and track participation of HELP volunteers and obtain feedback from participants</li>
	<li>Lead or arrange for appropriate alternate SVC representative attendance for each HELP volunteer trip</li>
</ul>

<h2>Inter(act) Volunteer Fair:</h2>
<ul>
	<li>Assist in coordination and promotion of the Inter(act) Fall semester volunteer fair with Director</li>
	<li>Lead organization and promotions of Winter Inter(act) volunteer fair</li>
</ul>

<h2>On-campus linkages:</h2>
<ul>
	<li>Collaborate with Student Housing Services, Interhall Council and Judicial Committees on the creation of positive volunteerism options for students receiving sanctions</li>
	<li>Collaborate with Interhall Council and Residence Life Staff to promote student volunteerism in residence</li>
	<li>Coordinate with University Departments and student organizations for the promotion of SVC</li>
</ul>

<h2>Do So Much Weekend:</h2>
<ul>
	<li>Work closely with Director to organize and host Do So Much Weekend, SVC’s annual student volunteerism mini-conference</li>
</ul>

<h2>Further duties:</h2>
<ul>
	<li>Organize and oversea promotions of SVC events through media outlets, through social media in collaboration with the Webmaster, and on-campus</li>
	<li>Required to complete thorough transition report and document library to be provided to incoming Projects Coordinator</li>
	<li>Attend staff and board training sessions as required</li>
	<li>Maintain day-to-day operations of SVC office, including answering phone, maintaining email presence, etc.</li>
	<li>Make space reservations, food services, and rentals as required for various events and programming</li>
	<li>Work in close collaboration with all other members of SVC’s staff and volunteer teams</li>
</ul>
