<h1>Kelsey Taylor - Community Relations Coordinator SVC, 2012 - 2013</h1>

<h2>One2One Referral Program:</h2>
<ul>
	<li>Complete and provide One2One volunteer referrals for students (online, in person, by phone)</li>
	<li>Track and provide statistics about the number of One2One referral completed throughout term</li>
	<li>Maintain up-to-date list of current opportunities in the local and international community</li>
	<li>Communicate with Guelph-Wellington volunteer centre to identify placements for skilled student volunteers when relevant</li>
</ul>

<h2>Organization &amp; Opportunity Database:</h2>
<ul>
	<li>Communicate regularly with the Webmaster for up-to-date volunteer listings, catalogues, and resources</li>
	<li>Update, develop, and promote University and Guelph/Wellington organization profiles and information</li>
	<li>Communicate regularly with the Volunteer Centre of Guelph-Wellington for community information, volunteer opportunities, future partnerships, and resources</li>
	<li>Communicate with Guelph-Wellington Volunteer Centre and community organizations to promote idea of skilled volunteerism opportunities</li>
</ul>

<h2>Volunteer Weekly Newsletter:</h2>
<ul>
	<li>Develop and email the ‘Volunteer Weekly’ to the SVC listserv and interested parties using online service such as www.verticalresponse.com</li>
</ul>

<h2>Further duties:</h2>
<ul>
	<li>Assistant in promoting services, programs and opportunities through various media methods</li>
	<li>Update and post volunteer opportunities and events on the SVC bulletin board in the University Centre</li>
	<li>Required to complete thorough transition report and document library to be provided to incoming Community Relations Coordinator</li>
	<li>Attend staff and board training sessions as required</li>
	<li>Maintain day-to-day operations of SVC office, including answering phone, maintaining email presence, etc.</li>
	<li>Work in close collaboration with all other members of SVC’s staff and volunteer teams</li>
</ul>
