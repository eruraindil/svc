<h1>Shawna Smith - Director SVC, 2012 - 2013</h1>

<!--<img src="../../imgs/team/director.jpg" />-->

<h2>Year-round duties (May - April)</h2>

<h3>Communications and Networking</h3>
<ul>
	<li>Act as SVC liaison for the Community Service Option with the Judicial Committee and Student Housing Services</li>
	<li>Communicate regularly with the Volunteer Centre of Guelph-Wellington to continue and expand current partnerships</li>
	<li>Work with University departments (e.g. Student Life) and organizations to promote and support volunteering initiatives</li>
	<li>Coordinate on-going development of Student Volunteer Connections and its outreach and collaboration with other campus and community stakeholders</li>
	<li>Sit on external organization committees as needed</li>
</ul>

<h3>Administration, Planning and Capacity Development</h3>
<ul>
	<li>Develop and implement volunteer and staff team professional development and oversee year-long activities</li>
	<li>Sit on SVC Board of Directors as non-voting member and participate in its ad hoc committees</li>
	<li>Provide Board of Directors with monthly activity reports, including updates on implementation of Board directives, SVC milestones to date and upcoming initiatives/objectives</li>
	<li>Ensure corporation forms and insurance are duly filed in coordination with the Board of Directors</li>
	<li>With the bookkeeper and Board Treasurer, maintain the financial records of Student Volunteer Connections of Guelph, Inc. (including fundraising and grant proposal writing as appropriate)</li>
	<li>Gather key performance indicators and develop feedback mechanisms for Student Volunteer Connections for intra-organization impact assessment</li>
</ul>

<h3>Marketing and Programming</h3>
<ul>
	<li>Oversee Student Volunteer Connections’ online Social Network presence</li>
	<li>Provide and participate in speaking engagements on and off campus as needed</li>
</ul>

<h2>Additional duties during Summer Semester (May – September)</h2>

<h3>Communication and Networking</h3>
<ul>
	<li>Liaise with Interhall Council, Student Housing and Residence Life Staff to develop outreach and information sharing with students entering residence in September</li>
	<li>Communicate and meet with community and on-campus agencies to expand knowledge and use of new online organization opportunity database</li>
	<li>Create and send out monthly e-newsletters to listserv, focusing on volunteerism resources</li>
</ul>

<h3>Administration, Planning and Capacity Development</h3>
<ul>
	<li>Create year-long strategic plan of SVC activities for revision with Board, paid and volunteer staff, assessing efficiency and effectiveness of existing services and programmes</li>
	<li>Seek Board advice to ensure planning falls in line with SVC mission and objectives</li>
	<li>Review and strengthen paid and volunteer staff training, recognition, and communication methods</li>
	<li>Create and implement summer programming that promotes volunteerism and raises awareness of volunteer issues amongst the local community</li>
	<li>Develop educational and reference materials for paid and volunteer staff members</li>
	<li>Recruit and coordinate training of any outstanding paid staff members, as well as volunteer team members</li>
</ul>

<h3>Promotion and Marketing</h3>
<ul>
	<li>General SVC promotion – resource fairs, RA training, IHC, Ontarion, and Orientation Week</li>
	<li>Promote SVC by updating and maintaining online tools including social networks (Twitter and	Facebook) and the SVC website</li>
</ul>

<h3>SVC Programming</h3>
<ul>
	<li>Organize and set up the Fall Semester Inter(act) volunteer fair Coordinate Orientation Week events and collaborations</li>
	<li>Host incoming orientation interviews and social to familiarize new 2012-2013 team to SVC and other team members</li>
	<li>Complete One2One Referral form submissions</li>
</ul>

<h2>Additional duties during Fall &amp; Winter Semesters (October – April)</h2>

<h3>Communication and Networking</h3>
<ul>
	<li>Network with community and on-campus groups (media, mentors, departments, organizations, and potential supporters) with the intention of seeking out new and updating old volunteer opportunities</li>
	<li>Maintain regular communication with University Departments and student organizations</li>
	<li>Meet with community-benefit organization representatives on a one-to-one basis</li>
</ul>

<h3>Administration, Planning and Capacity Development</h3>
<ul>
	<li>Complete staff contracts (September 2012) and the signing and distribution of bi-weekly pay cheques throughout year</li>
	<li>Organize regular staff meetings, training, and office schedules for paid and volunteer staff when appropriate</li>
	<li>Coordinate and complete transition for incoming 2013/2014 staff team in March 2013</li>
	<li>Participate in review of job descriptions and hiring process for 2013/2014 team</li>
</ul>

<h3>Promotion and Marketing</h3>
<ul>
	<li>Work with Webmaster to promote SVC through media outlets, including articles, news releases, advertisements, and coordinate online social media presence</li>
</ul>

<h3>SVC Programming</h3>
<ul>
	<li>Oversee coordination of Do So Much Weekend events and planning committee</li>
	<li>Organize volunteer and staff appreciation events</li>
</ul>
