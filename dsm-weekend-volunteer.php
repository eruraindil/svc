<h4>January 21 - 22, 2012, University of Guelph</h4>
<p><span style="color:#fc9005;font-weight: bold;">You can do so much.</span> <span style="color:#8ac53e;font-weight: bold;">This is your chance!</span> Do So Much Weekend is a <span style="color:#fc9005;font-weight: bold;">free conference</span> sparking and fostering <span style="color:#0397d7;font-weight: bold;">positive social action</span> through means of social innovation, participation, and collaboration. Do So Much Weekend is open to <span style="color:#fc9005;font-weight: bold;">students of all ages</span> from any educational institution in North America. <span style="color:#0397d7;font-weight: bold;">Get the tools</span> to make a positive change.</p>
<p><span style="color:#fc9005;font-weight: bold;">2012</span> is the year for <span style="color:#8ac53e;font-weight: bold;">breaking down the barriers</span> to engagement and <span style="color:#0397d7;font-weight: bold;">building community</span> near and far. A consistent question is; why is the general population not engaged in the issues that are happening in the world around us? Apathy is just a piece of the puzzle. Barriers within the system and personal barriers are another part of this complicated equation.</p>
<p>At the <strong>Do So Much Weekend</strong> delegates will not only have the opportunity to discuss these barriers, but they will also work together to discover ways in which to break down these barriers to engagement.</p>
