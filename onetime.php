<p>These one-time placements primarily serve as an opportunity for University of Guelph students who have been sanctioned by the Judicial Committee or Student Housing Services to pay off their fines through community service. In co-operation with the Judicial Committee and Student Housing Services of the University of Guelph, we assist sanctioned students with finding a low-committment volunteer placement within our network of community-benefit organizations. We do our best to match students based on their interests and degree programs in order to encourage them to stick with volunteering after their mandatory hours are complete.</p>
<p>However, these placements may also be utilized by students simply wishing to get involved in low-commitment volunteer positions or who are just beginning their first steps into volunteerism.</p>
