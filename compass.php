<p>Through capacity building and facilitating collaboration, we would like to see civic, social and environmental justice groups and community benefit organizations on campus expand their reach and build their resiliency. Our aim is to bring student groups together on campus to collaborate and learn from one another. We want to support the work that students are already doing, and build our training activities and other events in response to needs expressed by student groups.  In addition, we want to build an online calendar platform where groups can post their events so that students have a general resource to refer to, as well as helping groups plan their events in such a way as not to compete with one another.</p>

<h3>You are invited to the Compass Community Networking Event!</h3>

<p>Students and representatives from community organizations involved in social & environmental justice and civic engagement work are invited to the Community Networking Event. It is an opportunity for students and community organizers to have conversations about their work, share resources & strategies, expand on opportunities to collaborate and support each other in our work. </p>

