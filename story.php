<h2>our logo</h2>
<p>In 2009, SVC took on a new brand, ambition, and attitude in order to be more effective for the students and community of Guelph. Our new look has assisted us in bringing forth exciting new programs and campaigns to the students of the University of Guelph and the Guelph/Wellington Community.</p>
<img src="graphics/logomath.png" style="width:700px;height:203px;" alt="Ambitious Student + Shasta Daisy = UoG Volunteer Outlet!" />
<h3>explained</h3>
<p>The logo energetically represents SVC connecting a student to volunteerism. The student ambitously leaping forward, ready to take action, is connected to volunteerism, indicated by three green petals of the Shasta daisy, the national flower of volunteerism. The three petals represents the three regions SVC helps build community in; the University, the Guelph/Wellington County, and beyond.</p>
<p>The Shasta Daisy is viewed as a strong symbol of volunteerism. Introduced as the national flower of volunteerism during the International Year of the Volunteer (2001), the Shasta Daisy is a perennial flower which returns annually, keeps growing in size, and even after being seperated and replanted, continues to flourish. This easy-to-grow popular wild flower can reside in all regions of North America and can be planted in either spring or fall in almost any soil. Growth is slow in the first year but rapidly speeds up in the years following.</p>
<p>SVC has chosen to compliment the City of Guelph's official colours of green and blue. With over 100 collaborative efforts with community-benefit organizations in the community of Guelph/Wellington, we are connecting students all over the city who are leaving a positive impact. We find it important to represent how highly we respect and indulge in the many opportunities Guelph has to offer.</p>
